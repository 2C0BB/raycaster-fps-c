bool is_wall(int x, int y) {
    return map[y * mapX + x] == 1;
}

void draw_map2D() {
    for (int y = 0; y < mapY; y++) {
        for (int x = 0; x < mapX; x++) {

            if (is_wall(x, y)) {

                DrawRectangle(x * mapS, y * mapS, mapS, mapS, BLACK);   

            }
        }
    }
}
